void Components::init() {

	lighting_shader.use();
	lighting_shader.set_int("gPosition", 0);
	lighting_shader.set_int("gNormal", 1);
	lighting_shader.set_int("gAlbedoSpec", 2);

	cam.set_fov(glm::radians(45.0f));
	cam.set_ratio((GLfloat)1800 / (GLfloat)1600);
	cam.set_near(0.1f);
	cam.set_far(100.0f);
	cam.set_postion({ 0.0f, 0.0f,  3.0f });
	cam.set_front({ 0.0f, 0.0f, -1.0f });
	cam.set_up({ 0.0f, 1.0f, 0.0f });

}

